#include "ft_ls.h"

void	print_contents(t_file *file)
{
	int i;

	i = 1;
	while (file != NULL){
		printf("%s\t\t", file->name);
		if (i % 4 == 0)
			printf("\n");
		file = file->next;
		i++;
	}
}
