#include "ft_ls.h"

void	print_contents(t_file *head)
{
	int i;

	i = 1;
	while (head != NULL){
		printf("%s\t  ", head->name);
		if (i % 4 == 0)
			puts("");
		i++;
		head = head->next;
	}
}
